    private void jstoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jstoryActionPerformed
        // TODO add your handling code here:
        String caption = captiontextarea.getText();

        if (f == null) {
            JOptionPane.showMessageDialog(this, "Please select a photo");
        } else if (caption.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter caption");

        } else {
            try {

                HttpResponse<String> res = Unirest.post("http://localhost:8999/useraddstory")
                        .queryString("caption", caption)
                        .queryString("postid", postid)
                        .field("photo", f)
                        .asString();
                if (res.getStatus() == 200) {
                    String response = res.getBody();
                    System.out.println("Response---->" + response);
                    if (response.equals("SUCCESS")) {
                        JOptionPane.showMessageDialog(this, "Story added successfully");
                        getAllStories(postid);
                    } else {
                        JOptionPane.showMessageDialog(this, "Add Story failed");

                    }

                }
            } catch (Exception e) {
                e.printStackTrace();

            }
        }
    }//GEN-LAST:event_jstoryActionPerformed

