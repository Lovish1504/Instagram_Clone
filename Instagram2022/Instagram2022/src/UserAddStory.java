    public static BufferedImage imageToBufferedImage(Image img) {
        BufferedImage bi = new BufferedImage(img.getWidth(null), img.getHeight(null), BufferedImage.TYPE_INT_RGB);
        Graphics bg = bi.getGraphics();
        bg.drawImage(img, 0, 0, null);
        bg.dispose();
        return bi;
    }

    public void getAllStories(String postid) {
        try {
            HttpResponse<String> res = Unirest.get("http://localhost:8999/getallstories")
                    .queryString("postid", postid)
                    .asString();
            //-----//server working correctly
            if (res.getStatus() == 200) {
                String response = res.getBody();
                System.out.println("Response---->" + response);
                if (response.equals("SUCCESS")) {
                    JOptionPane.showMessageDialog(this, " added successfully");

                } else {
                    System.out.println("Response is---->" + response);
                    StringTokenizer st = new StringTokenizer(response, "~~");
                    int count = st.countTokens();
                    //int obj[]=new int[count];
                    mainpanel.setPreferredSize(new Dimension(400, count * 350));
                    addstorypanel obj[] = new addstorypanel[count];
                    int x = 10, y = 10;
                    mainpanel.removeAll();
                    for (int i = 0; i < count; i++) {
                        String userInfo = st.nextToken();
                        StringTokenizer st2 = new StringTokenizer(userInfo, "!!");
                        String caption = st2.nextToken();
                        String photo = st2.nextToken();
                        int storyid = Integer.parseInt(st2.nextToken());
                        System.out.println("caption-----" + caption);
                        System.out.println("photo-----" + photo);
                        //System.out.println("postid-----" + postid);
                        obj[i] = new addstorypanel();
                        obj[i].jphoto.setText(photo);
                        obj[i].jcaption.setText(caption);
                        try {
                            URL url = new URL(Global.ipAddress + "GetResource/" + photo);
                            //System.out.println("Path of Image Stored in server side--------------" + url);
                            BufferedImage img = ImageIO.read(url);
                            Image resizedimage = img.getScaledInstance(obj[i].jphoto.getWidth(), obj[i].jphoto.getHeight(), Image.SCALE_SMOOTH);
                            obj[i].jphoto.setIcon(new ImageIcon(resizedimage));

                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                        //when follow button is clicked/
                        obj[i].jdeletestorybtn.addActionListener(new ActionListener() {
                            @Override
                            public void actionPerformed(ActionEvent e) {

                                //code pending request
                                getAllStories(postid);
                                // useraddstory obj = new useraddstory(postid);
                            }
                        });
                        obj[i].setBounds(x, y, 400, 250);
                        mainpanel.add(obj[i]);
                        y += 350;
                    }
                    mainpanel.repaint();
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

