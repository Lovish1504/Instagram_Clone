    private void jeditprofileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jeditprofileActionPerformed
        // TODO add your handling code here:
        String email = jemail.getText();
        String phoneno = jphoneno.getText();

        if (email.isEmpty() || phoneno.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "All fieds are required.");

        } else if (email.indexOf("@") == -1) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email address");
        } else if (phoneno.length() != 10) {
            JOptionPane.showMessageDialog(this, "Please enetr a valid phone number");
        } else if (f == null) {
            try {
                HttpResponse<String> res = Unirest.get(Global.ipAddress + "usereditwithoutphoto")
                        .queryString("username", user)
                        .queryString("email", email)
                        .queryString("phoneno", phoneno)
                        .asString();
                if (res.getStatus() == 200) {
                    String response = res.getBody();
                    if (response.equals("SUCCESS")) {
                        JOptionPane.showMessageDialog(this, "Edit profile successfully!");
                        getData();

                    } else {
                        JOptionPane.showMessageDialog(this, "Edit profile Fails! \n Invalid email or phone number\nPlease enter again");
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            try {
                HttpResponse<String> res = Unirest.post(Global.ipAddress + "usereditwithphoto")
                        .queryString("username", user)
                        .queryString("email", email)
                        .queryString("phoneno", phoneno)
                        .field("photo", f)
                        .asString();
                if (res.getStatus() == 200) {
                    String response = res.getBody();
                    if (response.equals("SUCCESS")) {
                        JOptionPane.showMessageDialog(this, "Edit profile successfully!");
                        getData();

                    } else {
                        JOptionPane.showMessageDialog(this, "Edit profile Fails! \n Invalid email or phone number\nPlease enter again");
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jeditprofileActionPerformed

