    void getData() {
        try {
            HttpResponse<String> res = Unirest.get("http://localhost:8999/getdata")
                    .queryString("username", user)
                    .asString();
            if (res.getStatus() == 200) {
                String ans = res.getBody();
                System.out.println("response is--->" + ans);
                if (ans.equals("FAILS")) {
                    JOptionPane.showMessageDialog(this, "no data found");
                } else {
                    StringTokenizer st = new StringTokenizer(ans, ";;");
                    String email = st.nextToken();
                    String phoneno = st.nextToken();
                    String photo = st.nextToken();
                    jemail.setText(email);
                    jphoneno.setText(phoneno);
                   // jphoto.setText(photo);
                    try {
                        URL url = new URL(Global.ipAddress + "GetResource/" + photo);
                        //System.out.println("Path of Image Stored in server side--------------" + url);
                        BufferedImage img = ImageIO.read(url);
                        Image resizedimage = img.getScaledInstance(jphoto.getWidth(), jphoto.getHeight(), Image.SCALE_SMOOTH);
                        jphoto.setIcon(new ImageIcon(resizedimage));

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

