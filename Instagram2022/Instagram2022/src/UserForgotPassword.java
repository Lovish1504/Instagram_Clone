    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

         username = jusername.getText();
        if (username.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Username field is empty");
        } else {
            try {
                HttpResponse<String> res = Unirest.get("http://localhost:8999/userforgotpassword")
                        .queryString("username", username)
                        .asString();
                if (res.getStatus() == 200) {
                    String ans = res.getBody();

                    if (ans.equals("FAILS")) {
                        JOptionPane.showMessageDialog(rootPane, "Please enter a valid username");
                    } else {
                        otp = ans;
                        JOptionPane.showMessageDialog(rootPane, "OTP is " + otp);
                        jPanel1.setVisible(false);
                        jPanel2.setVisible(true);

                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

