    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String newpassword = jnewpassword.getText();
        String confirmpassword = jconfirmpassword.getText();
        if (newpassword.equals("") || confirmpassword.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "New Password & Confirm Password field is empty");
        } else {
            if (newpassword.equals(confirmpassword)) {
                try {
                    HttpResponse<String> res = Unirest.get("http://localhost:8999/usersetpassword")
                            .queryString("username", username)
                            .queryString("newpassword", newpassword)
                            .asString();

                    if (res.getStatus() == 200) {
                        String ans = res.getBody();
                        
                        if (ans.equals("UPDATED")) {
                            JOptionPane.showMessageDialog(rootPane, "Password Updated");
                            dispose();
                            UserLoginFrame obj =new UserLoginFrame();
                        } else {
                            JOptionPane.showMessageDialog(rootPane,"Something went wrong /n Error Occured");
                        }
                        
                    }
                    else{
                    JOptionPane.showMessageDialog(rootPane,"New Password & Confirm Password must be same");
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

